// AI DETECTION (GPTZero)
// ============================================================

class AIDetector {
  constructor(apiKey) {
    this.apiKey = apiKey;
  }

  async detect(text) {
    if (!this.apiKey) {
      // No detector configured — skip verification
      return { score: 0, human_score: 1, sentences: [], skipped: true };
    }

    const body = JSON.stringify({ document: text });
    const url = new URL('https://api.gptzero.me/v2/predict/text');

    return new Promise((resolve, reject) => {
      const req = https.request(url, {
        method: 'POST',
        headers: {
          'x-api-key': this.apiKey,
          'Content-Type': 'application/json',
          'Content-Length': Buffer.byteLength(body),
        },
        timeout: 30000,
      }, res => {
        let data = '';
        res.on('data', c => data += c);
        res.on('end', () => {
          try {
            const parsed = JSON.parse(data);
            const doc = parsed.documents?.[0] || {};
            resolve({
              score: doc.completely_generated_prob || 0,
              human_score: 1 - (doc.completely_generated_prob || 0),
              average_perplexity: doc.average_generated_prob || 0,
              sentences: (doc.sentences || [])
                .filter(s => s.generated_prob > 0.7)
                .map(s => ({ text: s.sentence, prob: s.generated_prob })),
            });
          } catch (e) {
            reject(new Error('Detection parse error: ' + e.message));
          }
        });
      });
      req.on('error', reject);
      req.on('timeout', () => { req.destroy(); reject(new Error('Detection timeout')); });
      req.write(body);
      req.end();
    });
  }
}

// ============================================================
// TEXT CHUNKING
// ============================================================

function chunkText(text, maxChunk = 3000) {
  const lines = text.split('\n');
  const chunks = [];
  let current = '';
  let inCodeBlock = false;

  for (const line of lines) {
    const trimmed = line.trim();
    if (trimmed.startsWith('```')) {
      inCodeBlock = !inCodeBlock;
      if (current.trim()) {
        chunks.push({ text: current.trim(), type: 'prose' });
        current = '';
      }
      if (!chunks.length || chunks[chunks.length - 1].type !== 'code') {
        chunks.push({ text: line, type: 'code' });
      } else {
        chunks[chunks.length - 1].text += '\n' + line;
      }
      continue;
    }

    if (inCodeBlock) {
      if (chunks.length && chunks[chunks.length - 1].type === 'code') {
        chunks[chunks.length - 1].text += '\n' + line;
      } else {
        chunks.push({ text: line, type: 'code' });
      }
      continue;
    }

    // Tables pass through unchanged
    if (trimmed.startsWith('|')) {
      if (current.trim() && !current.trim().endsWith('|')) {
        chunks.push({ text: current.trim(), type: 'prose' });
        current = '';
      }
      if (!chunks.length || chunks[chunks.length - 1].type !== 'table') {
        chunks.push({ text: line, type: 'table' });
      } else {
        chunks[chunks.length - 1].text += '\n' + line;
      }
      continue;
    }

    if (current.length + line.length + 1 > maxChunk && current.trim()) {
      chunks.push({ text: current.trim(), type: 'prose' });
      current = '';
    }
    current += line + '\n';
  }
  if (current.trim()) {
    chunks.push({ text: current.trim(), type: 'prose' });
  }
  return chunks;
}

