// MAIN HUMANIZE FUNCTION
// ============================================================

async function humanize(text, options = {}) {
  const llm = new LLMProvider({
    provider: options.llm_provider || 'openai',
    apiKey: options.llm_api_key,
    model: options.llm_model,
    baseUrl: options.llm_base_url,
  });

  const detector = new AIDetector(options.detector_api_key);
  const maxRetries = options.max_retries ?? 2;
  const targetScore = options.target_score ?? 0.3; // below 0.3 = likely human

  const chunks = chunkText(text, options.max_chunk_size || 3000);
  const results = [];
  const log = [];

  for (let i = 0; i < chunks.length; i++) {
    const chunk = chunks[i];

    if (chunk.type !== 'prose') {
      // Code blocks and tables pass through unchanged
      results.push(chunk.text);
      log.push({ chunk: i + 1, type: chunk.type, action: 'passthrough' });
      continue;
    }

    log.push({ chunk: i + 1, type: 'prose', chars: chunk.text.length });

    // Step 1: Initial humanize pass
    let humanized = await llm.complete(SYSTEM_PROMPT, chunk.text);
    log.push({ chunk: i + 1, action: 'initial_rewrite', output_chars: humanized.length });

    // Step 2: Detection loop
    for (let retry = 0; retry <= maxRetries; retry++) {
      const detection = await detector.detect(humanized);

      if (detection.skipped) {
        log.push({ chunk: i + 1, action: 'detection_skipped' });
        break;
      }

      log.push({
        chunk: i + 1,
        retry,
        ai_score: detection.score,
        human_score: detection.human_score,
        flagged_sentences: detection.sentences.length,
      });

      if (detection.score <= targetScore) {
        log.push({ chunk: i + 1, action: 'passed', score: detection.score });
        break;
      }

      if (retry === maxRetries) {
        log.push({ chunk: i + 1, action: 'max_retries_reached', final_score: detection.score });
        break;
      }

      // Step 3: Targeted rewrite of flagged sentences
      if (detection.sentences.length > 0) {
        const flaggedList = detection.sentences.map(s => `- "${s.text}" (${(s.prob * 100).toFixed(0)}% AI)`).join('\n');
        const prompt = REWRITE_FLAGGED_PROMPT
          .replace('{flagged}', flaggedList)
          .replace('{text}', humanized);
        humanized = await llm.complete(SYSTEM_PROMPT, prompt);
        log.push({ chunk: i + 1, action: 'targeted_rewrite', flagged: detection.sentences.length });
      } else {
        // No specific sentences flagged — full rewrite with higher temperature
        humanized = await llm.complete(SYSTEM_PROMPT, 'Rewrite this to sound MORE human and less predictable:\n\n' + humanized);
        log.push({ chunk: i + 1, action: 'full_rewrite' });
      }
    }

    results.push(humanized);
  }

  const output = results.join('\n\n');

  // Final detection on full text if detector is configured
  let finalDetection = null;
  if (options.detector_api_key) {
    try {
      // Only detect on prose portions
      const proseOnly = results.filter((_, i) => chunks[i].type === 'prose').join('\n\n');
      finalDetection = await detector.detect(proseOnly);
    } catch (e) {
      finalDetection = { error: e.message };
    }
  }

  return {
    humanized: output,
    chunks: chunks.length,
    detection: finalDetection,
    log,
  };
}

// ============================================================
// DETECT-ONLY FUNCTION
// ============================================================

async function detectOnly(text, apiKey) {
  const detector = new AIDetector(apiKey);
  return detector.detect(text);
}

module.exports = { humanize, detectOnly, chunkText, LLMProvider, AIDetector };
